/*2. Insert*/
CREATE PROCEDURE PR_City_INSERT
@CityId int,
@CityName varchar(250),
@Pincode varchar(6),
@StateId int,
@CityRemarks varchar(500)

AS
BEGIN
INSERT INTO MST_City(
CityId,
CityName,
Pincode,
StateId,
CityRemarks)

VALUES
(
@CityId,
@CityName,
@Pincode,
@StateId,
@CityRemarks
)

END

EXEC PR_City_INSERT 6,'Baroda','360008',5,'good'

/*3. Update*/
CREATE PROCEDURE PR_CITY_UPDATE
@CityId int,
@CityName varchar(250),
@Pincode varchar(6),
@StateId int,
@CityRemarks varchar(500)

AS
BEGIN
UPDATE MST_City SET
CityName=@CityName,
Pincode=@Pincode, 
StateId=@StateId,
CityRemarks=@CityRemarks

WHERE CityId=@CityId
END

EXEC PR_CITY_UPDATE 1,'jamnagar',300048,6,avg

